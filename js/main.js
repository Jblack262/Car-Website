function storePage() {
    var x = Math.floor(Math.random() * 5);
    if (x == 0) {
        window.location.href = "civic.html";
    } else if (x == 1) {
        window.location.href = "bugatti.html";
    } else if (x == 2) {
        window.location.href = "lambo.html";
    } else if (x == 3) {
        window.location.href = "mustang.html";
    } else if (x == 4) {
        window.location.href = "tesla.html";
    }
}